# ============================================================
# 1. REAL SCHEMA FOR THE LLM
# ============================================================

def get_schema_for_llm(conn):
    """
    Returns a complete and structured schema of the database.
    This schema is used as context for the LLM.
    """
    tables = list_tables(conn)
    schema = []

    for table in tables:
        columns = get_columns(conn, table)
        fks = get_foreign_keys(conn, table)

        schema.append(f"Table: {table}")

        for _, col in columns.iterrows():
            line = f"- {col['name']} | {col['type']} | PK:{col['pk']} | NULL:{col['notnull']}"
            schema.append(line)

        if not fks.empty:
            schema.append("  FOREIGN KEYS:")
            for _, fk in fks.iterrows():
                schema.append(f"    - {fk['from']} -> {fk['table']}.{fk['to']}")

        schema.append("")

    return "\n".join(schema)


# ============================================================
# 1.1 Clean SQL
# ============================================================

def clean_sql(text: str) -> str:
    """
    Cleans SQL generated by the LLM:
    - Removes ```sql and ``` blocks
    - Removes -- and /* */ comments
    - Extracts the full query even if it is multi-line
    - Returns only valid SQL
    """

    # Remove code blocks
    text = text.replace("```sql", "")
    text = text.replace("```", "")
    text = text.strip()

    # Remove single-line comments --
    text = re.sub(r"--.*", "", text)

    # Remove multi-line comments /* ... */
    text = re.sub(r"/\*.*?\*/", "", text, flags=re.DOTALL)

    # Find the first valid SQL keyword
    sql_start = None
    keywords = [
        "SELECT", "INSERT", "UPDATE", "DELETE",
        "CREATE", "DROP", "ALTER", "WITH"
    ]

    lines = text.split("\n")
    for i, line in enumerate(lines):
        if any(line.strip().upper().startswith(k) for k in keywords):
            sql_start = i
            break

    if sql_start is None:
        return ""

    # Rebuild SQL from the line where it starts
    sql = "\n".join(lines[sql_start:]).strip()

    # Remove trailing dot if the model adds it
    if sql.endswith("."):
        sql = sql[:-1]

    return sql.strip()


