# ============================================================
# 1. Generate ultra-technical ERD (Mermaid.js)
# ============================================================

def generate_erd(conn) -> str:
    """
    Generates a technical ERD diagram in Mermaid.js format based on the structure
    of a SQLite database.

    Args:
        conn (sqlite3.Connection): Active database connection.

    Returns:
        str: Mermaid code ready to be rendered in Streamlit.

    Example:
        mermaid_code = generate_erd(conn)
        print(mermaid_code)
        # Output: "erDiagram ..."
    """

    tables = list_tables(conn)

    # Modern & colorful Mermaid style
    mermaid_code = """
    %%{init: {
      'theme': 'forest',
      'themeVariables': {
        'primaryColor': '#ffffff',
        'primaryTextColor': '#0f172a',
        'primaryBorderColor': '#1e293b',
        'lineColor': '#334155',
        'fontSize': '32px',
        'fontFamily': 'Inter, Arial, sans-serif',
        'edgeLabelBackground': '#e2e8f0',
        'entityBorderColor': '#1e293b',
        'entityBackground': '#f8fafc'
      },
      'er': {
        'layoutDirection': 'TB',
        'rankSep': 95,
        'nodeSep': 75,
        'useMaxWidth': true
      }
    }}%%
    erDiagram
    """

    relationships = []

    for table in tables:
        columns = get_columns(conn, table)
        fks = get_foreign_keys(conn, table)
        unique_cols = get_unique_columns(conn, table)

        table_id = table.replace(" ", "_")
        mermaid_code += f'    {table_id} {{\n'

        for _, col in columns.iterrows():
            name = col["name"]
            col_type = col["type"]

            tags = []
            if col["pk"]:
                tags.append("PK")
            if name in unique_cols:
                tags.append("UK")

            fk_match = fks[fks["from"] == name] if not fks.empty else None
            if fk_match is not None and not fk_match.empty:
                tags.append("FK")
                dest_table = fk_match.iloc[0]["table"].replace(" ", "_")
                relationships.append(
                    f'    {table_id} ||--o{{ {dest_table} : "{name}"'
                )

            null_info = "NOT_NULL" if col["notnull"] else "NULL"
            default_val = f"DEF:{col['dflt_value']}" if col["dflt_value"] else ""
            extra_info = f"{null_info} {default_val}".strip()

            mermaid_code += (
                f'        {col_type} {name} {",".join(tags)} "{extra_info}"\n'
            )

        mermaid_code += "    }\n"

    mermaid_code += "\n".join(set(relationships))
    return mermaid_code


