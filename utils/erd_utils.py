# 2. Render & download ERD in Streamlit
# ============================================================

def erd_to_image(mermaid_code):
    """
    Renders a Mermaid.js ERD diagram in Streamlit and allows downloading it as SVG.

    Args:
        mermaid_code (str): Mermaid code generated by generate_erd().

    Returns:
        None
    """

    html_code = f"""
    <style>
        #canvas-area {{
            background: #ffffff;
            border: 2px solid #e2e8f0;
            border-radius: 12px;
            padding: 30px;
            overflow: auto;
            max-height: 750px;
        }}

        #mermaid-renderer svg {{
            width: auto !important;
            height: auto !important;
            max-width: 100%;
            transform: scale(1.20);
            transform-origin: top left;
        }}

        .mermaid .icon {{
            display: none !important;
        }}
    </style>

    <div id="canvas-area">
        <div id="mermaid-renderer" class="mermaid">
            {mermaid_code}
        </div>
    </div>

    <div style="
        margin-top: 25px;
        display: flex;
        justify-content: space-between;
        align-items: center;
        font-family: Inter, sans-serif;
    ">
        <button id="download-btn" style="
            background-color: #2563eb;
            color: white;
            padding: 12px 24px;
            border: none;
            border-radius: 8px;
            cursor: pointer;
            font-weight: bold;
            font-size: 14px;
        ">
            💾 Download SVG (High Resolution)
        </button>

        <div style="
            font-size: 14px;
            color: #475569;
            background: #f8fafc;
            padding: 10px 14px;
            border-radius: 6px;
            border: 1px solid #e2e8f0;
        ">
            <b style="color:#dc2626;">PK</b>: Primary |
            <b style="color:#2563eb;">FK</b>: Foreign |
            <b style="color:#16a34a;">UK</b>: Unique |
            <b>DEF</b>: Default
        </div>
    </div>

    <script type="module">
        import mermaid from 'https://cdn.jsdelivr.net/npm/mermaid@10/dist/mermaid.esm.min.mjs';

        mermaid.initialize({{
            startOnLoad: true,
            er: {{ useMaxWidth: true, minEntityWidth: 200 }}
        }});

        document.getElementById('download-btn').addEventListener('click', function() {{
            const svgElement = document.querySelector('#mermaid-renderer svg');
            if (svgElement) {{
                const bbox = svgElement.getBBox();
                const svgClone = svgElement.cloneNode(true);
                svgClone.setAttribute("viewBox", `${{bbox.x}} ${{bbox.y}} ${{bbox.width}} ${{bbox.height}}`);
                svgClone.setAttribute("width", bbox.width);
                svgClone.setAttribute("height", bbox.height);
                svgClone.style.backgroundColor = "white";

                const serializer = new XMLSerializer();
                let source = serializer.serializeToString(svgClone);
                const url = "data:image/svg+xml;charset=utf-8," + encodeURIComponent(source);
                const link = document.createElement("a");
                link.href = url;
                link.download = "DB_Diagram_Technical.svg";
                link.click();
            }}
        }});
    </script>
    """

    components.html(html_code, height=950, scrolling=False)
